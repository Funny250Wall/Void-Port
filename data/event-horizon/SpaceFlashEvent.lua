function onBeatHit()
    if curBeat == 136 then
        triggerEvent("SpaceFlash", 0, 0)
    elseif curBeat == 200 then
        triggerEvent("SpaceFlash", 0, 0)
    elseif curBeat == 264 then
        triggerEvent("SpaceFlash", 0, 0)
    elseif curBeat == 328 then
        triggerEvent("SpaceFlash", 0, 0)
    elseif curBeat == 368 then
        triggerEvent("SpaceFlash", 0, 0)
    elseif curBeat == 372 then
        triggerEvent("SpaceFlash", 0, 0)
    elseif curBeat == 392 then
        triggerEvent("SpaceFlash", 0, 0)
    end
end